<?php

namespace App\Listeners;

class ChangeExperience
{
    /**
     * Create the event listener.
     *
     * @return void
     */
    public function __construct()
    {
        //
    }

    /**
     * Handle the event.
     *
     * @param  $event
     * @return void
     */
    public function handle($event)
    {
        $event->changeExperience($event->exp);
    }
}
